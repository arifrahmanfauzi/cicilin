Kami mengacu kepada AAOIFI Shariah Standards dan Fatwa Dewan Syariah MUI tentang Murabahah<br><br>
Proses di cicil.in tidak meminjamkan uang untuk membeli barang, namun membeli barang kemudian menjual kepada pembeli dengan cicilan.<br><br>
cicil.in mendapatkan keuntungan dengan mengambil margin profit oleh karena itu harga cicilan lebih mahal daripada harga pasar. cicil.in tidak mengenakan bunga.<br><br>
cicil.in membeli barang dari supplier, setelah pembayaran lunas dan secara prinsip menjadi milik cicil.in, barang tersebut kemudian dijual kepada pembeli. cicil.in tidak menjual barang yang belum menjadi kepemilikan cicil.in.
