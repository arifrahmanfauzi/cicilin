Apa itu Akad Murabahah?<br><br>

Murabahah adalah perjanjian jual-beli antara penjual dengan pembeli. Penjual membeli barang yang diperlukan pembeli kemudian menjualnya kepada pembeli yang bersangkutan sebesar harga perolehan ditambah dengan margin keuntungan yang disepakati antara penjual dan pembeli.<br><br>

cicil.in berusaha mengikuti kaidah syariah Islam sesuai dengan visi kami untuk menyediakan solusi keuangan syariah bagi masyarakat yang membutuhkan.<br><br>

